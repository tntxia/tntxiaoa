package com.tntxia.oa.common;

public class CommonOpen {
	
	private String url;
	
	private String target;
	
	private String winOpt;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getTarget() {
		return target;
	}

	public void setTarget(String target) {
		this.target = target;
	}

	public String getWinOpt() {
		return winOpt;
	}

	public void setWinOpt(String winOpt) {
		this.winOpt = winOpt;
	}
	
	

}
