package com.tntxia.oa.entity;

public class Payment {
	
	private String contact;
	
	private String orderform;
	
	private String sup_number;
	
	private String supplier;
	
	private String hb;
	
	private String man;
	
	private String deptjb;

	public String getOrderform() {
		return orderform;
	}

	public void setOrderform(String orderform) {
		this.orderform = orderform;
	}

	public String getContact() {
		return contact;
	}

	public void setContact(String contact) {
		this.contact = contact;
	}

	public String getSup_number() {
		return sup_number;
	}

	public void setSup_number(String sup_number) {
		this.sup_number = sup_number;
	}

	public String getSupplier() {
		return supplier;
	}

	public void setSupplier(String supplier) {
		this.supplier = supplier;
	}

	public String getHb() {
		return hb;
	}

	public void setHb(String hb) {
		this.hb = hb;
	}

	public String getMan() {
		return man;
	}

	public void setMan(String man) {
		this.man = man;
	}

	public String getDeptjb() {
		return deptjb;
	}

	public void setDeptjb(String deptjb) {
		this.deptjb = deptjb;
	}
	
	

}
