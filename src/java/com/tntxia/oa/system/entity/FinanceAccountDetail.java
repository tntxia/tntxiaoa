package com.tntxia.oa.system.entity;

public class FinanceAccountDetail {
	
	private int id;
	
	private int kmId;
	
	private String km;
	
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getKmId() {
		return kmId;
	}

	public void setKmId(int kmId) {
		this.kmId = kmId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getKm() {
		return km;
	}

	public void setKm(String km) {
		this.km = km;
	}
	
	

}
