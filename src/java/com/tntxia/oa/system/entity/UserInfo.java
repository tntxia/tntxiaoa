package com.tntxia.oa.system.entity;

public class UserInfo {
	
	private String username;
	
	private String dept;
	
	private String deptjb;
	
	private String restrain_name;
	
	private int restrainId;

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getDept() {
		return dept;
	}

	public void setDept(String dept) {
		this.dept = dept;
	}

	public String getDeptjb() {
		return deptjb;
	}

	public void setDeptjb(String deptjb) {
		this.deptjb = deptjb;
	}

	public String getRestrain_name() {
		return restrain_name;
	}

	public void setRestrain_name(String restrain_name) {
		this.restrain_name = restrain_name;
	}

	public int getRestrainId() {
		return restrainId;
	}

	public void setRestrainId(int restrainId) {
		this.restrainId = restrainId;
	}
	
	

}
