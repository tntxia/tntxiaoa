package com.tntxia.oa.system.entity;

public class ManageItem {
	
	private int id;
	
	private String name;
	
	private String url;
	
	private String target;
	
	private String winOpt;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getTarget() {
		return target;
	}

	public void setTarget(String target) {
		this.target = target;
	}

	public String getWinOpt() {
		return winOpt;
	}

	public void setWinOpt(String winOpt) {
		this.winOpt = winOpt;
	}
	
	

}
