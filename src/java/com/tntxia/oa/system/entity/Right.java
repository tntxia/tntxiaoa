package com.tntxia.oa.system.entity;

public class Right {
	
	private int id;
	
	private String rightName;
	
	private String rightDisplay;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getRightName() {
		return rightName;
	}

	public void setRightName(String rightName) {
		this.rightName = rightName;
	}

	public String getRightDisplay() {
		return rightDisplay;
	}

	public void setRightDisplay(String rightDisplay) {
		this.rightDisplay = rightDisplay;
	}
	
	

}
