package com.tntxia.oa.warehouse.entity;

public class SupplierPrice {
	
	private int id;
	
	private int pro_sup_id;
	
	private String pro_sup_name;
	
	private double pro_sup_price;
	
	private String pro_sup_hb;
	
	private String pro_sup_rate;
	
	private String pro_sup_date;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getPro_sup_id() {
		return pro_sup_id;
	}

	public void setPro_sup_id(int pro_sup_id) {
		this.pro_sup_id = pro_sup_id;
	}

	public String getPro_sup_name() {
		return pro_sup_name;
	}

	public void setPro_sup_name(String pro_sup_name) {
		this.pro_sup_name = pro_sup_name;
	}

	public double getPro_sup_price() {
		return pro_sup_price;
	}

	public void setPro_sup_price(double pro_sup_price) {
		this.pro_sup_price = pro_sup_price;
	}

	public String getPro_sup_hb() {
		return pro_sup_hb;
	}

	public void setPro_sup_hb(String pro_sup_hb) {
		this.pro_sup_hb = pro_sup_hb;
	}

	public String getPro_sup_rate() {
		return pro_sup_rate;
	}

	public void setPro_sup_rate(String pro_sup_rate) {
		this.pro_sup_rate = pro_sup_rate;
	}

	public String getPro_sup_date() {
		return pro_sup_date;
	}

	public void setPro_sup_date(String pro_sup_date) {
		this.pro_sup_date = pro_sup_date;
	}
	
}
